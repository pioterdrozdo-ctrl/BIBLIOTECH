'use strict';

const assert = require('node:assert/strict');
const fs = require('node:fs');
const path = require('node:path');

const root = path.join(__dirname, '..');
const read = file => fs.readFileSync(path.join(root, file), 'utf8');

const server = read('backend/server.js');
const inventoryRoute = read('backend/routes/inventory.js');
const documentsRoute = read('backend/routes/documents.js');
const passkeyRoute = read('backend/routes/passkeys.js');
const pdfService = read('backend/services/pdfDocuments.js');
const adminPage = read('frontend/admin.html');
const operationsUi = read('frontend/js/library-operations.js');
const adminWorkspaceUi = read('frontend/js/admin-workspace.js');
const adminWorkspaceCss = read('frontend/css/admin-workspace.css');
const passkeyLogin = read('frontend/js/passkey-login.js');
const loginPage = read('frontend/index.html');
const aboutPage = read('frontend/about.html');
const securityUi = read('frontend/js/profile-security.js');
const booksRoute = read('backend/routes/books.js');
const serviceWorker = read('frontend/sw.js');
const interfaceLanguage = read('frontend/js/interface-language.js');

assert.ok(server.includes("app.use('/api/inventory', inventoryRoutes)"));
assert.ok(server.includes("app.use('/api/documents', documentRoutes)"));
assert.ok(server.includes("app.use('/api/auth', passkeyRoutes)"));
assert.match(inventoryRoute, /router\.use\(authMiddleware, isAdmin\)/);
assert.match(documentsRoute, /router\.use\(authMiddleware, isAdmin\)/);
assert.ok(inventoryRoute.includes("result = 'duplicate'"));
assert.ok(inventoryRoute.includes("result = 'misplaced'"));
assert.ok(inventoryRoute.includes("result = 'unknown'"));
assert.ok(inventoryRoute.includes("IN (10, 13)"), 'unknown codes must not match books with an empty ISBN');
assert.ok(pdfService.includes('createLabelsPdf'));
assert.ok(pdfService.includes('createInventoryReportPdf'));
assert.ok(pdfService.includes('createWriteoffActPdf'));
assert.ok(pdfService.includes('createRentalActPdf'));
assert.ok(pdfService.includes('book.qr_code || book.qrCode'));
assert.ok(pdfService.includes('book.qrLink || book.qr_link || storedQrCode'), 'printed QR must prefer the camera-friendly link');
assert.ok(pdfService.includes("text(storedQrCode || 'Без QR'"), 'the existing stored code must remain visible on the label');
assert.ok(documentsRoute.includes("url.searchParams.set('book', String(bookId))"), 'printed QR links must open a book card');
assert.ok(documentsRoute.includes('qrLink: buildBookLink(req, book.id)'));
assert.ok(!documentsRoute.includes('UPDATE books SET qr_code'), 'PDF generation must not rewrite QR codes');
assert.ok(!inventoryRoute.includes('UPDATE books SET qr_code'), 'inventory must not rewrite QR codes');
assert.ok(booksRoute.includes('if (!book.qr_code)'), 'existing QR codes must remain unchanged during book updates');
assert.ok(adminPage.includes('id="inventoryPanelTitle"'));
assert.ok(adminPage.includes('id="documentsPanelTitle"'));
assert.ok(adminPage.includes('vendor/jsQR.js'));
assert.ok(adminPage.includes('id="inventoryWorkspaceModal"'));
assert.ok(adminPage.includes('id="documentsWorkspaceModal"'));
assert.ok(adminPage.includes('data-admin-section="overview"'));
assert.ok(adminPage.includes('id="languageSwitcher"'), 'admin page must keep the language control visible');
assert.ok(adminPage.includes('js/interface-language.js'), 'admin page must load the shared language controller');
assert.ok(adminWorkspaceUi.includes("sessionStorage.setItem('bibliotech-admin-section'"));
assert.ok(adminWorkspaceCss.includes('.admin-command-grid'));
assert.ok(operationsUi.includes('navigator.mediaDevices.getUserMedia'));
assert.ok(operationsUi.includes('/documents/inventory/'));
assert.ok(passkeyRoute.includes('verifyRegistrationResponse'));
assert.ok(passkeyRoute.includes('verifyAuthenticationResponse'));
assert.ok(passkeyRoute.includes("userVerification: 'required'"));
assert.ok(passkeyLogin.includes('navigator.credentials.get'));
assert.ok(passkeyLogin.includes('PublicKeyCredential.isConditionalMediationAvailable'));
assert.ok(passkeyLogin.includes("mediation: 'conditional'"));
assert.ok(passkeyLogin.includes("usernameInput?.addEventListener('change', tryUsernamePasskey)"));
assert.ok(passkeyLogin.includes('window.startAutomaticPasskey = startAutomaticPasskey'));
assert.ok(!passkeyLogin.includes("addEventListener('click', loginWithPasskey)"), 'passkey login must not require a separate button');
assert.ok(loginPage.includes('autocomplete="username webauthn"'));
assert.ok(!loginPage.includes('id="passkeyLoginBtn"'), 'the separate passkey login button must be removed');
assert.ok(!aboutPage.includes('@AndreyDorozhkin'), 'removed Telegram contact must not remain on the about page');
assert.ok(aboutPage.includes('@ks_221b'), 'the remaining Telegram contact must be preserved');
assert.ok(securityUi.includes('navigator.credentials.create'));
assert.ok(securityUi.includes('Обычный пароль останется доступен'));
assert.ok(serviceWorker.includes("'/js/library-operations.js'"));
assert.ok(serviceWorker.includes("'/css/library-operations.css'"));
assert.ok(serviceWorker.includes("'/js/admin-workspace.js'"));
assert.ok(serviceWorker.includes("'/css/admin-workspace.css'"));
assert.ok(serviceWorker.includes("'/js/interface-language.js'"));
assert.ok(interfaceLanguage.includes('window.BibliotechI18n'));
assert.ok(interfaceLanguage.includes('MutationObserver'), 'dynamic admin content must be translated after it opens');
assert.ok(interfaceLanguage.includes("adminHeading: 'Manage BIBLIOTECH'"));

console.log('Library operations check OK: inventory, link-based printed QR labels, admin workspaces and passkeys are integrated without rewriting stored QR codes.');
